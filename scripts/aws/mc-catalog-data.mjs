function displayName(category, raw) {
  let n = raw.replace(/^MC /, "").trim();
  const prefix = `${category} `;
  if (n.startsWith(prefix)) n = n.slice(prefix.length).trim();
  return n;
}

function p(category, code, name, sizeCm, image, featured = false) {
  const id = `mc-${code.toLowerCase().replace(/[^a-z0-9]+/g, "-").replace(/^-|-$/g, "")}`;
  const title = displayName(category, name);
  const cls = CLASSIFICATION[code];
  if (!cls) throw new Error(`분류 누락: ${code}`);
  const nameKo = KO_NAMES[code];
  if (!nameKo) throw new Error(`한국어 이름 누락: ${code}`);
  return {
    id,
    name: title,
    nameKo,
    category,
    bodyPart: cls.bodyPart,
    bodyDetail: cls.bodyDetail,
    driveType: cls.driveType,
    brand: BRAND,
    price: null,
    summary: `${nameKo} (${title}). 모델 ${code}.`,
    specs: [
      { label: "모델", value: code },
      ...(sizeCm ? [{ label: "크기 (cm)", value: sizeCm }] : []),
      { label: "운동 부위", value: `${cls.bodyPart} · ${cls.bodyDetail}` },
      { label: "구동 방식", value: cls.driveType },
    ],
    image,
    featured,
    status: "active",
  };
}

